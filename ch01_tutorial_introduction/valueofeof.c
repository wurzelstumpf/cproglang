#include <stdio.h>

/* The C Programming Language, Kernighan and Ritchie, 2nd Edition 1988
 *
 * 1.5 Character Input and Output
 *
 * Exercise 1-7
 *
 *    Write a program to print the value of EOF
 *
 */

int main()
{
    printf("Value of EOF as     integer is: %d\n", EOF);
    printf("Value of EOF as       octal is: %o\n", EOF);
    printf("Value of EOF as hexadecimal is: %x\n", EOF);
}
